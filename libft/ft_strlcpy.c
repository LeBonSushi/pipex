#include "libft.h"

size_t	ft_strlcpy(char *d, const char *s, size_t n)
{
	size_t	i;

	i = 0;
	if (n > 0)
	{
		while (s[i] && i < (n - 1))
		{
			d[i] = s[i];
			i++;
		}
		d[i] = 0;
	}
	while (s[i])
		i++;
	return (i);
}
